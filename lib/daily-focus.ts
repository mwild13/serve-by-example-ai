export function getDailyFocus(isManager: boolean): DayFocus & { dayIndex: number } {
  const start = new Date(new Date().getFullYear(), 0, 0);
  const dayOfYear = Math.floor((Date.now() - start.getTime()) / 86400000);
  const index = dayOfYear % 30;
  const focus = isManager ? MANAGEMENT_FOCUS_30[index] : STAFF_FOCUS_30[index];
  return { ...focus, dayIndex: index + 1 };
}
